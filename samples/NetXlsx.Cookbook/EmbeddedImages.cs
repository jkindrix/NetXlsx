// Cookbook recipe — EmbeddedImages (v1.1 / decision I-52).
//
// Anchor a PNG and a JPEG to single cells at natural pixel size. Uses
// auto-detection from magic bytes for the PNG path; explicit
// ImageFormat for the JPEG path. Both round-trip through Workbook.Open
// as picture-data entries.

using System;
using System.Threading.Tasks;

namespace NetXlsx.Cookbook.Recipes;

/// <summary>
/// Builds a one-sheet workbook with two embedded images. Uses the
/// canonical tiny-PNG and a hand-assembled 1×1 JPEG as the byte source
/// so the recipe needs no external fixture files.
/// </summary>
public static class EmbeddedImages
{
    /// <summary>Output sheet name.</summary>
    public const string SheetName = "WithImages";

    // 67-byte canonical 1×1 transparent PNG.
    private static readonly byte[] s_tinyPng = Convert.FromBase64String(
        "iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mNk" +
        "YAAAAAYAAjCB0C8AAAAASUVORK5CYII=");

    // Minimal 1×1 grayscale JPEG (~125 bytes).
    private static readonly byte[] s_tinyJpeg = new byte[]
    {
        0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01,
        0x01, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0xFF, 0xDB, 0x00, 0x43,
        0x00, 0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08, 0x07, 0x07, 0x07, 0x09,
        0x09, 0x08, 0x0A, 0x0C, 0x14, 0x0D, 0x0C, 0x0B, 0x0B, 0x0C, 0x19, 0x12,
        0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D, 0x1A, 0x1C, 0x1C, 0x20,
        0x24, 0x2E, 0x27, 0x20, 0x22, 0x2C, 0x23, 0x1C, 0x1C, 0x28, 0x37, 0x29,
        0x2C, 0x30, 0x31, 0x34, 0x34, 0x34, 0x1F, 0x27, 0x39, 0x3D, 0x38, 0x32,
        0x3C, 0x2E, 0x33, 0x34, 0x32, 0xFF, 0xC0, 0x00, 0x0B, 0x08, 0x00, 0x01,
        0x00, 0x01, 0x01, 0x01, 0x11, 0x00, 0xFF, 0xC4, 0x00, 0x14, 0x00, 0x01,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0xFF, 0xC4, 0x00, 0x14, 0x10, 0x01, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
        0x00, 0x00, 0xFF, 0xDA, 0x00, 0x08, 0x01, 0x01, 0x00, 0x00, 0x3F, 0x00,
        0xB2, 0xC0, 0x07, 0xFF, 0xD9,
    };

    /// <summary>Runs the recipe.</summary>
    public static async Task Run(string outputPath)
    {
        using var wb = Workbook.Create();
        var sh = wb.AddSheet(SheetName);

        sh["A1"].SetString("Logo:");
        // Auto-detect from magic bytes — 2-arg overload.
        sh.AddPicture("B1", s_tinyPng);

        sh["A5"].SetString("Thumbnail:");
        // Explicit format — 3-arg overload.
        sh.AddPicture("B5", s_tinyJpeg, ImageFormat.Jpeg);

        await wb.SaveAsync(outputPath);
    }
}
